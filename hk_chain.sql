CREATE TABLE hk_chain_codes (
    chain_num NUMBER PRIMARY KEY,
    chain_code VARCHAR2(4) NOT NULL UNIQUE,
    is_active CHAR(1) DEFAULT 'Y',
    FOREIGN KEY (chain_num) REFERENCES hk_chains(chain_num)
);

